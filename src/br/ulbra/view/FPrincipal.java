    private void btnGerarFolhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarFolhaActionPerformed
        String func;
        int qtdH, qtdHE, qtdF;
        double valorH, bruto, salHE, salF, descINSS, descIR, salLiq;
        
        func = txtNome.getText();
        qtdH = Integer.parseInt(txtQtdHoras.getText());
        qtdF = Integer.parseInt(txtQtdFilhos.getText());
        qtdHE = Integer.parseInt(txtQtdHorasE.getText());
        valorH = Double.parseDouble(txtVHora.getText());
        
        bruto = qtdH * valorH;
        salHE = qtdHE * valorH * 1.5;
        salF = qtdF * 15;
        if(bruto <= 1320.00)
        {
            descINSS = (bruto * 7.5) / 100;
        }
        else if(bruto > 1320.00 && bruto < 2571.29)
        {
            descINSS = (bruto * 9.0) / 100;
        }
        else if(bruto > 2571.29 && bruto < 3856.94)
        {
            descINSS = (bruto * 12.0) / 100;
        }
        else
        {
            descINSS = (bruto * 14.0) / 100;
        }
        
        if(bruto <= 1903.09)
        {
            descIR = 0;
        }
        else if(bruto > 1903.09 && bruto < 2826.65)
        {
            descIR = (bruto * 7.5) / 100;
        }
        else if(bruto > 2826.65 && bruto < 3751.05)
        {
            descIR = (bruto * 15.0) / 100;
        }
        else if(bruto > 3751.05 && bruto < 4664.68)
        {
            descIR = (bruto * 22.5) / 100;
        }
        else
        {
            descIR = (bruto * 27.5) / 100;
        }
        
        salLiq = bruto + salF + salHE - descIR - descINSS;
        
        lbRFunc.setText(func);
        lbRQtdHoras.setText(String.valueOf(qtdH));
        lbRSalBruto.setText("R$" + String.valueOf(bruto));
        lbRINSS.setText("R$" + String.valueOf(descINSS));
        lbRIR.setText("R$" + String.valueOf(descIR));
        lbRQtdHorasEx.setText(String.valueOf(qtdHE));
        lbRValHorasEx.setText("R$" + String.valueOf(salHE));
        lbRValSalFamilia.setText("R$" + String.valueOf(salF));
        lbRSalLiq.setText("R$" + String.valueOf(salLiq));
    }//GEN-LAST:event_btnGerarFolhaActionPerformed

